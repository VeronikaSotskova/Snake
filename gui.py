    def create_img(self):
        """Создает изображение на основании списка"""
        screen = pygame.Surface((Const.SCREEN_WIDTH, Const.SCREEN_HEIGHT), pygame.SRCALPHA, 32)
        x = 1
        y = 1
        for i in self.level:
            if i == 0:
                pygame.draw.rect(screen, pygame.Color("Grey"), pygame.Rect(x,y,Const.SIZE_BLOCK,Const.SIZE_BLOCK))
            x += Const.SIZE_BLOCK + Const.SIZE_SPACE
            if x == Const.SCREEN_WIDTH:
                y += Const.SIZE_BLOCK + Const.SIZE_SPACE
                x = 1
        pygame.image.save(screen, "images/gui2.png")

    def draw_level(self, window):
        """Отрисовывает уровень игры"""
        window.blit(self.gui_image, (0, 0))

    def init_field(self):
        """Заполняет списки с координатами"""
        x = 1
        y = 1
        for i in self.level:
            if i == 0:
                self.barrier.append([x, y])
            elif i == 1:
                self.field.append([x, y])
            x += Const.SIZE_BLOCK + Const.SIZE_SPACE
            if x == 441:
                y += Const.SIZE_BLOCK + Const.SIZE_SPACE
                x = 1

    def get_new_indicator(self):
        """Заполняет список координатами индикатора"""
        self.indicator.append([self.indicator[-1][0] + Const.SIZE_BLOCK + Const.SIZE_SPACE, 34])

    def draw_indicator(self, window):
        """Отрисовка индикатора"""
        for i in self.indicator:
            pygame.draw.rect(window, pygame.Color("Green"), pygame.Rect(i[0], i[1], Const.SIZE_BLOCK, Const.SIZE_BLOCK))

    def draw_win(self, window):
        """Создает победный экран"""
        window.blit(self.win, (23, 100))

    def check_win_or_lose(self):
        """Отслеживает выиграл или проиграл игрок"""
        if len(self.indicator) == 0:
            self.game = "LOSE"
        elif len(self.indicator) == 37:
            self.game = "WIN"

    def show_score(self, window):
        """Отображение результата"""
        print_text('Score: {0}'.format(len(self.indicator)-1), 1, 5, pygame.Color("Red"), 'monaco', 24, window)
